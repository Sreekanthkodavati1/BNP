using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Bnp.Core.Tests.API.CDIS_Services.CDIS_TestCases
{
    [TestClass]
    public class BTA229_CDIS_GetRewardCatalogCount : ProjectTestBase
    {
        TestCase testCase;
        List<TestStep> listOfTestSteps = new List<TestStep>();
        TestStep testStep;

        [TestMethod]
        public void BTA229_CDIS_GetRewardCatalogCount_PositiveCase()

        {
            testCase = new TestCase(TestContext.TestName);
            listOfTestSteps = new List<TestStep>();
            testStep = new TestStep();
            String stepName = "";
            Common common = new Common(this.DriverContext);
            CDIS_Service_Methods cdis_Service_Method = new CDIS_Service_Methods(common);

            try
            {
                Logger.Info("Test Method Started");
                testStep = TestStepHelper.StartTestStep(testStep);
                stepName = "Get Reward Count with CDIS service where locationgroup is null";
                // Reward Count gets the reward count without StoreLocation (LocationGroup is null) and without Reward Type as "Payment" (DB value =1)
                int rewardCatalogCount = cdis_Service_Method.GetRewardCatalogCount();
                testStep.SetOutput("The RewardCount: " + rewardCatalogCount+" is returned from the GetRewardCatalogCount method");
                Logger.Info("RewardCount: " + rewardCatalogCount);
                testStep = TestStepHelper.EndTestStep(testCase, testStep, stepName, true, DriverContext.SendScreenshotImageContent("API"));
                listOfTestSteps.Add(testStep);


                testStep = TestStepHelper.StartTestStep(testStep);
                stepName = "Validating reward count with data from database where locationgroup is null";
                string dbresponse = DatabaseUtility.GetRewardCatalogCountfromDBUsingIdSOAP();
                Assert.AreEqual(rewardCatalogCount + "", dbresponse, "Expected Value is " + rewardCatalogCount + " Actual Value is " + dbresponse);
                testStep.SetOutput("Reward Count from db is : " + dbresponse);
                testStep = TestStepHelper.EndTestStep(testCase, testStep, stepName, true, DriverContext.SendScreenshotImageContent("API"));
                listOfTestSteps.Add(testStep);

                testCase.SetStatus(true);
            }
            catch (Exception e)
            {
                testStep = TestStepHelper.EndTestStep(testCase, testStep, stepName, false, DriverContext.SendScreenshotImageContent("API"));
                listOfTestSteps.Add(testStep);
                testCase.SetStatus(false);
                testCase.SetErrorMessage(e.Message);
                Assert.Fail(e.Message.ToString());
            }
            finally
            {
                testCase.SetTestCaseSteps(listOfTestSteps);
                testCase.SetEndTime(new StringHelper().GetFormattedDateTimeNow());
                listOfTestCases.Add(testCase);
            }
        }
    }
}